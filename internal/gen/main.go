// Command gen drives the schema-agnostic graphql-opgen generator
// (github.com/trackness/graphql-opgen) over the vendored Stash SDL, emitting
// genqlient operations and fragments, an operation manifest, and a machine-facing
// catalog. It runs as the first step of `go generate` (see generate.go), ahead of
// genqlient itself, supplying the Stash-specific Config (see stashConfig).
package main

import (
	"flag"
	"fmt"
	"os"
	"path/filepath"

	"github.com/lightning-rider-999/go-stash/internal/exitcode"
	"github.com/lightning-rider-999/go-stash/schema"
	genops "github.com/trackness/graphql-opgen"
)

const header = "# Code generated by genops; DO NOT EDIT.\n\n"

// stashConfig is the Stash-specific genops configuration: the schema-agnostic
// compiler in github.com/trackness/graphql-opgen derives the structural surface, and this Config
// supplies everything particular to Stash — the exit-code vocabulary, the CLI
// naming irregulars, the audited path-named exceptions, and the target package
// the emitted command table references.
func stashConfig() genops.Config {
	return genops.Config{
		ExitCodes: genops.ExitCodeProvider{
			Base:               exitCodeNames(exitcode.Base),
			NotFound:           exitcode.NotFound.Name,
			DestructiveRefused: exitcode.DestructiveRefused.Name,
			JobFailed:          exitcode.JobFailed.Name,
			StillRunning:       exitcode.StillRunning.Name,
			Unconfirmed:        exitcode.Unconfirmed.Name,
		},
		Naming:               stashNaming(),
		PathNamedAllowlist:   stashPathNamedAllowlist(),
		TargetPackageImport:  "github.com/lightning-rider-999/go-stash/stash",
		OperationConstSuffix: "_Operation",
		CommandTableDoc: []string{
			"generatedCommands is the full operation table, one spec per Stash root",
			"field, sorted by OpName. buildRootCommand assembles these into the cobra",
			"tree. Query is the genqlient operation const, so the query text lives in",
			"exactly one place.",
		},
	}
}

// exitCodeNames projects the names out of a slice of taxonomy codes, in order.
func exitCodeNames(codes []exitcode.Code) []string {
	names := make([]string, len(codes))
	for i, c := range codes {
		names[i] = c.Name
	}
	return names
}

// stashNaming carries Stash's entity-vocabulary irregulars: the small curated
// set the regular structural rules (trailing-"s" plurals, <entity><verb>
// mutations, prefix groups) cannot infer. Stash's vocabulary is regular enough
// that only the irregular plurals (Galleries), multi-word nouns (SceneMarkers,
// SavedFilters), pinned subscriptions, and the exact-match finds need stating.
func stashNaming() genops.NamingRules {
	return genops.NamingRules{
		PluralEntity: map[string]string{
			"Galleries":    "gallery",
			"SceneMarkers": "scene-marker",
			"SavedFilters": "saved-filter",
		},
		SingularEntity: map[string]string{
			"SceneMarker": "scene-marker",
			"SavedFilter": "saved-filter",
		},
		SubscriptionPaths: map[string][]string{
			"jobsSubscribe":         {"job", "watch"},
			"loggingSubscribe":      {"log", "tail"},
			"scanCompleteSubscribe": {"scan", "watch"},
		},
		VerbSuffixes: map[string]string{
			"Create":  "create",
			"Update":  "update",
			"Destroy": "destroy",
			"Merge":   "merge",
		},
		PluralMutationLeaf: map[string]string{
			"Destroy": "destroy-many",
			"Update":  "update-many",
			"Merge":   "merge-many",
		},
		ExactFinds: map[string][]string{
			"findDuplicateScenes":   {"scene", "list-duplicates"},
			"findScenesByPathRegex": {"scene", "list-by-path-regex"},
			"findSceneByHash":       {"scene", "get-by-hash"},
			"findDefaultFilter":     {"saved-filter", "get-default"},
			"findJob":               {"job", "get"},
		},
		IrregularPluralNoun: map[string]string{
			"galleries": "gallery",
		},
		ExactGroups: map[string][]string{
			"configuration": {"config", "get"},
		},
		PrefixGroups: []genops.PrefixGroupRule{
			{Prefix: "configure", Group: "config"},
			{Prefix: "metadata", Group: "metadata"},
			{Prefix: "scrape", Group: "scrape"},
			{Prefix: "submitStashBox", Group: "stash-box", LeafPrefix: "submit-", MatchExact: true},
			{Prefix: "stashBox", Group: "stash-box"},
		},
		FallbackGroup: "misc",
	}
}

// stashPathNamedAllowlist is the audited set of Stash object/union/interface
// types genops may emit as path-named inline selections rather than canonical
// fragment types (spike finding A3), each mapped to the reason it is exempt from
// the "no path-named struct" invariant: mixed-selection junction wrappers,
// union/interface fields decoded by a __typename-keyed UnmarshalJSON, and
// value-type cycles in the file/folder graph terminated scalars-only.
func stashPathNamedAllowlist() map[string]string {
	return map[string]string{
		// Mixed-selection junction wrappers (no id + object edge).
		"SceneGroup":       "junction {group: Group!, scene_index} — Scene.groups",
		"SceneMovie":       "junction {movie: Movie!, scene_index} — Scene.movies (deprecated)",
		"GroupDescription": "junction {group: Group!, description} — Group.containing_groups / sub_groups",
		"SceneMarkerTag":   "junction {tag: Tag!, scene_markers: [SceneMarker!]!} — Query.sceneMarkerTags",
		"PluginHook":       "junction {plugin: Plugin!, ...} — plugin hooks",
		"PluginTask":       "junction {plugin: Plugin!, ...} — plugin tasks",

		// Union- and interface-typed fields (__typename-keyed decode, A5/A4).
		"VisualFile": "union VideoFile | ImageFile — Image.visual_files",
		"BaseFile":   "interface (BasicFile|VideoFile|ImageFile|GalleryFile) — findFile / findFiles / Folder.zip_file",

		// Value-type cycles in the file/folder graph (terminated scalars-only, B6).
		"Folder":    "self-referential folder tree — parent_folder / parent_folders / sub_folders",
		"BasicFile": "file metadata cycling via parent_folder / zip_file",
	}
}

func main() {
	schemaDir := flag.String("schema", "schema", "vendored SDL directory")
	overlayPath := flag.String("overlay", "internal/gen/overlay.yaml", "curated overlay file")
	flag.Parse()

	if err := run(*schemaDir, *overlayPath); err != nil {
		_, _ = fmt.Fprintln(os.Stderr, "genops:", err)
		os.Exit(1)
	}
}

func run(schemaDir, overlayPath string) error {
	cfg := stashConfig()
	c, err := genops.Compile(schemaDir, overlayPath, schema.SchemaVersion, cfg)
	if err != nil {
		return err
	}

	manifestJSON, err := c.Manifest.JSON()
	if err != nil {
		return err
	}
	catalogJSON, err := c.Catalog.JSON()
	if err != nil {
		return err
	}
	commandsGo, err := genops.EmitCommands(c.Manifest, cfg)
	if err != nil {
		return err
	}

	for _, w := range []struct {
		path    string
		content []byte
	}{
		{"internal/gen/generated/fragments.graphql", []byte(header + c.Fragments)},
		{"internal/gen/generated/operations.graphql", []byte(header + c.Operations)},
		{"internal/gen/generated/manifest.json", manifestJSON},
		{"schema/catalog.json", catalogJSON},
		// A byte-identical copy lives beside the CLI so `stash catalog` can
		// embed it with go:embed (which cannot reach `..` paths). The check
		// gate diffs both against a fresh `task generate` to keep them in sync.
		{"cmd/stash/catalog.json", catalogJSON},
		{"cmd/stash/gen_commands.go", commandsGo},
	} {
		if err := writeFile(w.path, w.content); err != nil {
			return err
		}
	}
	return nil
}

func writeFile(path string, b []byte) error {
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, b, 0o644)
}
